package Commands;

/**
 * Интерфейс {@code Command} задаёт общий контракт для всех команд программы.
 *
 * <p>Используется в реализации паттерна "Команда" (Command pattern),
 * позволяя инкапсулировать запросы в виде объектов.</p>
 *
 * <p>Каждая команда должна:
 * <ul>
 *     <li>иметь уникальное имя</li>
 *     <li>предоставлять описание</li>
 *     <li>уметь выполнять действие с аргументом</li>
 * </ul>
 */

public interface Command {

    /**
     * Возвращает имя команды.
     *
     * @return имя команды (используется для вызова)
     */
    String getName();

    /**
     * Возвращает описание команды.
     *
     * @return описание команды (используется в help)
     */
    String getDescription();

    /**
     * Выполняет команду.
     *
     * @param arg аргумент команды (может быть {@code null})
     */
    String execute(Object arg);

}